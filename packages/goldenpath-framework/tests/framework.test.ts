// ── Workflow Tests ───────────────────────────────────────────────────────────

describe('PR Pipeline Generator', () => {
  it('should generate a workflow with required jobs', () => {
    const workflow = generatePRPipeline({
      serviceName: 'transactionify',
      language: 'python',
      cdkStack: 'TransactionifyStack',
    });

    expect(workflow.name).toContain('transactionify');
    expect(workflow.jobs).toHaveProperty('detect-work-id');
    expect(workflow.jobs).toHaveProperty('small-tests');
    expect(workflow.jobs).toHaveProperty('security-scan');
    expect(workflow.jobs).toHaveProperty('standards-check');
  });

  it('should include sandbox deployment when enabled', () => {
    const workflow = generatePRPipeline({
      serviceName: 'transactionify',
      language: 'python',
      cdkStack: 'TransactionifyStack',
      enableSandboxDeploy: true,
    });

    expect(workflow.jobs).toHaveProperty('deploy-sandbox');
    const deployJob = workflow.jobs['deploy-sandbox'];
    expect(deployJob?.if).toContain('deploy:sandbox');
  });

  it('should validate as a correct workflow', () => {
    const workflow = generatePRPipeline({
      serviceName: 'transactionify',
      language: 'python',
    });

    const errors = validateWorkflow(workflow);
    expect(errors).toHaveLength(0);
  });
});

describe('Integration Pipeline Generator', () => {
  it('should generate staging and production jobs', () => {
    const workflow = generateIntegrationPipeline({
      serviceName: 'transactionify',
      language: 'python',
      cdkStack: 'TransactionifyStack',
      stagingRoleArn: 'arn:aws:iam::123:role/staging',
      productionRoleArn: 'arn:aws:iam::123:role/production',
    });

    expect(workflow.jobs).toHaveProperty('deploy-staging');
    expect(workflow.jobs).toHaveProperty('deploy-production');
    expect(workflow.jobs).toHaveProperty('smoke-tests');
  });

  it('should trigger on main branch pushes', () => {
    const workflow = generateIntegrationPipeline({
      serviceName: 'transactionify',
      language: 'python',
      cdkStack: 'TransactionifyStack',
      stagingRoleArn: 'arn:aws:iam::123:role/staging',
      productionRoleArn: 'arn:aws:iam::123:role/production',
    });

    expect(workflow.on).toHaveProperty('push');
    // eslint-disable-next-line @typescript-eslint/no-unnecessary-type-assertion
    expect((workflow.on as Record<string, unknown>).push).toEqual(
      expect.objectContaining({ branches: ['main'] })
    );
  });
});

