// ── CDK Construct Tests ──────────────────────────────────────────────────────

describe('ServiceStack', () => {
  it('should synthesize without errors', () => {
    const app = new App();

    const stack = new ServiceStack(app, 'TestStack', {
      serviceName: 'test-service',
      team: 'platform',
      environment: 'sandbox',
      runtime: 'python3.12',
      language: 'python',
      handlers: [
        { name: 'Process', path: 'src/process.py' },
      ],
      apiRoutes: [
        { path: '/process', method: 'POST', handler: 'Process' },
      ],
      enableDynamoDB: true,
    });

    expect(stack.api).toBeDefined();
    expect(stack.functions.has('Process')).toBe(true);
    expect(stack.table).toBeDefined();
    expect(stack.topic).toBeDefined();
  });

  it('should tag resources correctly', () => {
    const app = new App();
    const stack = new ServiceStack(app, 'TestStack2', {
      serviceName: 'test-service',
      team: 'platform',
      environment: 'staging',
      runtime: 'nodejs20.x',
      language: 'typescript',
      handlers: [{ name: 'Handler', path: 'src/index.ts' }],
      apiRoutes: [{ path: '/', method: 'GET', handler: 'Handler' }],
    });

    const template = Template.fromStack(stack);
    const lambda = template.findResources('AWS::Lambda::Function');
    const handlerKey = Object.keys(lambda).find((k) => k.startsWith('Handler'));
    expect(handlerKey).toBeDefined();
    // eslint-disable-next-line @typescript-eslint/no-unsafe-member-access
    const tags = lambda[handlerKey!].Properties.Tags as Array<{ Key: string; Value: string }>;
    const tagMap = Object.fromEntries(tags.map((t) => [t.Key, t.Value]));
    expect(tagMap['service']).toBe('test-service');
    expect(tagMap['team']).toBe('platform');
    expect(tagMap['managed-by']).toBe('goldenpath-framework');
  });
});

// ── DORA Telemetry Tests ─────────────────────────────────────────────────────

describe('DoraTelemetry', () => {
  it('should record deployment events', () => {
    const telemetry = new DoraTelemetry({ project: 'tx' });
    const event = telemetry.recordDeployment({
      workId: 'FIN-123',
      commitSha: 'abc123',
      environment: 'staging',
      success: true,
    });

    expect(event.eventType).toBe('deployment');
    expect(event.workId).toBe('FIN-123');
    expect(event.success).toBe(true);
    expect(event.environment).toBe('staging');
    expect(event.timestamp).toBeDefined();
  });

  it('should record failure events', () => {
    const telemetry = new DoraTelemetry({ project: 'tx' });
    const event = telemetry.recordFailure({
      workId: 'FIN-456',
      commitSha: 'def789',
      errorMessage: 'Deployment timeout',
    });

    expect(event.eventType).toBe('failure');
    expect(event.success).toBe(false);
    expect(event.metadata).toHaveProperty('error', 'Deployment timeout');
  });

  it('should create audit context', () => {
    const audit = DoraTelemetry.createAuditContext({
      actor: 'dev@example.com',
      action: 'deploy',
      reason: 'release',
    });

    expect(audit.actor).toBe('dev@example.com');
    expect(audit.action).toBe('deploy');
    expect(audit.complianceFramework).toBe('soc2');
  });
});
